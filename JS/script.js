function generateStars(note) {
  let stars = "";
  for (let i = 1; i <= 5; i++) {
    stars += i <= note ? "★" : "☆";
  }
  return stars;
}

function showCategory(cat) {
  const content = document.getElementById("content");
  const title = document.getElementById("category-title");

  title.textContent = cat.toUpperCase();

  content.innerHTML = "";

  let items = [];
  if (cat === "Tous") items = boissons;
  else items = autresCategories[cat];

  if (items.length === 0) {
    content.innerHTML = "<p>Pas d'éléments pour cette catégorie.</p>";
    return;
  }

  items.forEach(item => {
    const drink = document.createElement("div");
    drink.className = "drink";
    drink.innerHTML = `
      <img src="${item.img}" alt="${item.nom}">
      <div class="stars">${generateStars(item.note)}</div>
      <div class="name">${item.nom}</div>
      <div class="price">${item.prix}</div>
      <div class="btn-orange"><a class="icon" href="https://wa.me/+237699740930?text=Bonjour, j'aime bien vos elements "><i class="fa-brands fa-whatsapp"></i> Commandez</div>
    `;
    content.appendChild(drink);
  });
}
